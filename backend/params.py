m1 = 'Agency'
m1_subtext = "Internal Locus of Control"
m1_spiel = '''
Agency is a master trait. It predicts execution better than intelligence, and determines whether other virtues ever get deployed.

High-agency people behave as if the world is malleable. They may acknowledge constraints, but they don’t emotionally submit to them. When something fails, they instinctively look for the lever they missed rather than the excuse that absolves them. They move before they’re ready, correct mid-flight, and take responsibility even when no one is watching.

Low-agency people can be smart, kind, and articulate — and still inert. Their stories are populated by forces acting on them: systems, people, timing, luck. Nothing is ever quite their fault, and therefore nothing is ever quite their power.
'''

m2 = 'Horizon'
m2_subtext = 'Compounding Time and Orientation'
m2_spiel = '''
Horizon is a vector composed of distance and direction. It's the depth and length of the game you play.

Long-horizon thinkers understand the compounding returns of the long game. They understand second and third-order effects, making choices today that only make sense years from now. They are patient without being passive, and ambitious without being frantic. They understand that effort can stack, that reputation accumulates, and that small advantages, protected long enough, become overwhelming.

Short-horizon people optimize for relief, status, or validation. They think in loops rather than long lines. They reset their lives frequently — new plans, new identities, new goals — without carrying much forward. They confuse motion with progress and intensity with direction.
'''

m3 = 'Vision'
m3_subtext = 'Quality of World Model'
m3_spiel = '''
Vision is the ability to face reality without flinching or dressing it up. It's the accuracy of your perception of reality.

High scorers care more about being correct than being consistent. They can entertain uncomfortable  and controversial opinions, but will change their mind without shame when evidence shifts, always separating moral preference from factual belief. Their confidence comes from contact with reality, not from rhetorical fluency.

Low scorers defend narratives. They argue to win, not to learn. They treat disagreement as a threat to identity, and evidence as something to be selectively adopted. They will confuse moral preference with truth, protecting identity over accuracy. They can be fluent, confident, intelligent, yet wrong.
'''
